import os
from dotenv import load_dotenv
# Импортируем официальный клиент для работы с Gemini
from google import genai 
# Импортируем типы данных, чтобы строго описать инструменты для модели
from google.genai import types

# 1. Загружаем переменные окружения из файла .env (там лежит наш API-ключ)
load_dotenv()

# 2. Инициализируем клиента Gemini
client = genai.Client()

# =====================================================================
# НАШ ИНСТРУМЕНТ (TOOL): Простая функция на Python
# Мы снабжаем её подробным описанием (Docstring) и указанием типов (Type Hinting).
# Gemini прочитает это описание и поймет, зачем нужна эта функция и что ей передавать.
# =====================================================================
def get_product_price(product_name: str) -> str:
    """
    Позволяет узнать цену товара в нашем магазине по его точному названию.
    
    Args:
        product_name: Название товара (например, "яблоки", "молоко", "хлеб").
    """
    # Переводим название в нижний регистр, чтобы избежать ошибок с заглавными буквами
    product = product_name.lower()
    
    # Наша импровизированная "база данных" цен
    prices_db = {
        "яблоки": "120 рублей за кг",
        "молоко": "85 рублей за бутылку",
        "хлеб": "45 рублей за буханку"
    }
    
    # Проверяем, есть ли товар в нашей базе данных
    if product in prices_db:
        return f"Цена товара '{product_name}': {prices_db[product]}"
    else:
        return f"Товар '{product_name}' не найден в ассортименте магазина."


# =====================================================================
# ГЛАВНЫЙ ЗАПУСК
# =====================================================================

# Текст, который написал пользователь. Он хочет узнать цену яблок.
user_query = "Привет! Подскажи, пожалуйста, сколько у вас стоят яблоки?"

print(f"Пользователь спрашивает: {user_query}\n")

# Отправляем запрос модели, передавая ей нашу функцию в списке config.tools
response = client.models.generate_content(
    model='gemini-3.1-flash-lite',
    contents=user_query,
    # Мы буквально говорим Gemini: "Если тебе не хватает знаний, ты можешь использовать вот эту функцию"
    config=types.GenerateContentConfig(
        tools=[get_product_price],
    )
)

# Смотрим, что ответила модель на первом этапе
print("--- Ответ от модели (сырые данные) ---")
# Проверяем, решила ли модель вызвать функцию (tool_calls)
if response.function_calls:
    print("🤖 Модель решила вызвать функцию!")
    for call in response.function_calls:
        print(f"   Название функции, которую хочет вызвать ИИ: {call.name}")
        print(f"   Аргументы, которые она подобрала: {call.args}")
        
        # Получаем имя функции в виде строки
        func_name = call.name
        # Получаем аргументы (в нашем случае это product_name)
        func_args = call.args
        
        # Вызываем нашу реальную Python-функцию с аргументами от ИИ
        # Мы используем func_args['product_name'], так как Gemini вернула словарь
        real_result = get_product_price(product_name=func_args['product_name'])
        print(f"\n⚙️ Выполняем функцию на компьютере... Результат: {real_result}")
else:
    print("🤖 Модель ответила обычным текстом (функции не вызывались):")
    print(response.text)